    /**
     * use value in the memory
     * 
     * @param evt 
     */
    private void GetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GetButtonActionPerformed
        // TODO add your handling code here:
        
        if(MemoryList.getModel().getSize() == 0){
            JOptionPane.showMessageDialog(NoticeFrame, "Fill Memory");
        }
        else if(MemoryList.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(NoticeFrame, "Select Memory");
        }
        else{
            InputAndOutputFields.setText(MemoryList.getSelectedValue());
            for(memoryCollection x : list){
                if(x.result.equals(MemoryList.getSelectedValue())){
                    FormulaFields.setText(x.formula);
                }
            }
        }
    }//GEN-LAST:event_GetButtonActionPerformed

    
    
