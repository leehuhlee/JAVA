    /**
     * Change number to show
     * @param evt 
     */
    private double update(){
        double number;
        
        if(DecRadioButton.isSelected()){
             number = Double.parseDouble(InputAndOutputFields.getText());
        }
        else{
            number = compute();
        }
        return number;
    }
    
    /**
     * Compute different number base
     * @param evt 
     */
    private double compute(){
        String str = InputAndOutputFields.getText();
        double index=1;
        double number = 0;
        
        
        for(int i=str.length()-1; i>=0; i--){
            if(str.charAt(i) - 48  == 17){
                number += 10*index;
            }
            else if(str.charAt(i) - 48  == 18){
                number += 11*index;
            }
            else if(str.charAt(i) - 48  == 19){
                number += 12*index;
            }
            else if(str.charAt(i) - 48  == 20){
                number += 13*index;
            }
            else if(str.charAt(i) - 48  == 21){
                number += 14*index;
            }
            else if(str.charAt(i) - 48  == 22){
                number += 15*index;
            }
            else{
                number += (str.charAt(i) - 48) * index;
            }
            
            if(HexRadioButton.isSelected()){
                index *= 16;
            }
            else if(DecRadioButton.isSelected()){
                index *= 10;
            }
            else if(OctRadioButton.isSelected()){
                index *= 8;
            }
            else{
                index *= 2;
            }
        }
        
        return number;
    }

    /**
     * Erase Decimal point in double
     * @param evt 
     */
    private String EraseMark(double number){
        number = Math.round(number*10000000000d)/10000000000d;
        
        if(HexRadioButton.isSelected()){
            return notationCompute((int)number, 16);
        }
        else if(DecRadioButton.isSelected()){
            if(number == (long) number)
                return String.format("%d",(long)number);
            else{
                return String.format("%s",number);
            }
        }
        else if(OctRadioButton.isSelected()){
            return notationCompute((int)number, 8);
        }
        else{
            return notationCompute((int)number, 2);
        }
    }
    
     /**
     * Compute different number base notation
     * @param evt 
     */
    private String notationCompute(int number, int notation){
        String hex="";
        int modnum;
        if(number==0){
            hex="0";
        }
        
        while(number>0){
            modnum = number % notation; 
            if(modnum==10){
                hex = "A"+hex;
            }
            else if(modnum==11){
                hex = "B"+hex;
            }
            else if(modnum==12){
                hex = "C"+hex;
            }
            else if(modnum==13){
                hex = "D"+hex;
            }
            else if(modnum==14){
                hex = "E"+hex;
            }
            else if(modnum==15){
                hex = "F"+hex;
            }
            else{
                hex = modnum + hex;
            }
            number = number/notation;
        }
        return hex;
    }
    
    /**
     * Calculate formula
     * @param evt 
     */
    private void EqualSignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualSignButtonActionPerformed
        // TODO add your handling code here:
        
        if(expression.equals("")){
            JOptionPane.showMessageDialog(NoticeFrame, "Input Number");
            return;
        }
        else if(operation.equals("")){
            JOptionPane.showMessageDialog(NoticeFrame, "Input operation");
            return;
        }
        secondnumber = update();
        
        FormulaFields.setText(EraseMark(firstnumber) + " " + operation + " " + EraseMark(secondnumber) + " " + "=");
        if(operation.equals("+")){
            result = firstnumber + secondnumber;
        }
        else if(operation.equals("-")){
            result = firstnumber - secondnumber;
        }
        else if(operation.equals("*")){
            result = firstnumber * secondnumber;
        }
        else if(operation.equals("/")){
            result = firstnumber / secondnumber;
        }
        else if(operation.equals("mod")){
            result = firstnumber % secondnumber;
        }
        
        InputAndOutputFields.setText(EraseMark(result));
        firstnumber = update();
        
        InsertMemory();
        
        secondnumber = 0;
        expression = "";
        operation = "=";
    }//GEN-LAST:event_EqualSignButtonActionPerformed

    /**
     * memory storing the result of the last computation.
     */
    private void InsertMemory(){
        memoryCollection memory = new memoryCollection(InputAndOutputFields.getText(), FormulaFields.getText());
        list.add(memory);
        
        dlm.addElement(EraseMark(result));
        MemoryList.setModel(dlm);
    }
    
