     /**
     * input sin
     * @param evt 
     */
    private void SinusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SinusButtonActionPerformed
        // TODO add your handling code here:
        if(operation !="="&&expression.equals("")){
            JOptionPane.showMessageDialog(NoticeFrame, "Input Number");
            return;
        }
        else{
            firstnumber = update();
            FormulaFields.setText(SinusButton.getText() + "( " + EraseMark(firstnumber) + " )");
            result = Math.sin(Math.toRadians(firstnumber));
            InputAndOutputFields.setText(EraseMark(result));
            operation="=";
            expression = "";
            InsertMemory();
        }
    }//GEN-LAST:event_SinusButtonActionPerformed

